package piped_io_Stream;

public class Line {

	private int ejeX;
	private int ejeY;
	
	public Line(int ejeX, int ejeY) {
		super();
		this.ejeX = ejeX;
		this.ejeY = ejeY;
	}

	public int getEjeX() {
		return ejeX;
	}

	public void setEjeX(int ejeX) {
		this.ejeX = ejeX;
	}

	public int getEjeY() {
		return ejeY;
	}

	public void setEjeY(int ejeY) {
		this.ejeY = ejeY;
	}
	
	
}
